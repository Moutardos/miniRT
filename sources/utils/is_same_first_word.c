#include "minirt.h"

bool	is_same_first_word(const char *s1, const char *s2)
{
	while (*s1 == *s2)
	{
		if (*s1 == '\0' || *s1 == ' ')
			return (1);
		s1++;
		s2++;
	}
	if ((*s1 == ' ' && *s2 == '\0') || (*s2 == ' ' && *s1 == '\0'))
		return (1);
	return (0);
}
